interface LunarCacheItem {
  ts: number;
  lunarMonth: number;
  baseYear: number;
  nextTs: number;
}

let _lunarCache: LunarCacheItem[] | null = null;

function buildLunarCache(): LunarCacheItem[] {
  if (_lunarCache) return _lunarCache;
  
  _lunarCache = [];
  const years = Object.keys(LUNAR_BASE).map(Number).sort();
  
  years.forEach((y) => {
    const rows = LUNAR_BASE[y];
    let prevM = 0;
    let curYear = y;
    
    rows.forEach((md, idx) => {
      const [m, d] = md;
      if (m < prevM) curYear = y + 1;
      prevM = m;
      const ts = Date.UTC(curYear, m - 1, d);
      _lunarCache!.push({ ts, lunarMonth: idx + 1, baseYear: y, nextTs: 0 });
    });
  });
  
  // nextTs 설정
  _lunarCache.forEach((item, i) => {
    item.nextTs = _lunarCache![i + 1] ? _lunarCache![i + 1].ts : item.ts + 30 * 86400000;
  });
  
  return _lunarCache;
}

export interface LunarDate {
  month: number;
  day: number;
}

export function getLunarDay(solarDate: Date): LunarDate | null {
  const ts = Date.UTC(solarDate.getFullYear(), solarDate.getMonth(), solarDate.getDate());
  const cache = buildLunarCache();
  
  for (let i = 0; i < cache.length; i++) {
    if (ts >= cache[i].ts && ts < cache[i].nextTs) {
      const day = Math.floor((ts - cache[i].ts) / 86400000) + 1;
      return { month: cache[i].lunarMonth, day };
    }
  }
  
  return null;
}
